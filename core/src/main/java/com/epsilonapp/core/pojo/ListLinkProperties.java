package com.epsilonapp.core.pojo;

/**
 * The Class ListLinkProperties.
 */
public class ListLinkProperties {

	/** The label. */
	private String label;

	/** The url. */
	private String url;

	/** The new window. */
	private String newWindow;

	/**
	 * Gets the label.
	 *
	 * @return the label
	 */
	public String getLabel() {
		return label;
	}

	/**
	 * Sets the label.
	 *
	 * @param label
	 *            the new label
	 */
	public void setLabel(String label) {
		this.label = label;
	}

	/**
	 * Gets the url.
	 *
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}

	/**
	 * Sets the url.
	 *
	 * @param url
	 *            the new url
	 */
	public void setUrl(String url) {
		this.url = url;
	}

	/**
	 * Gets the new window.
	 *
	 * @return the new window
	 */
	public String getNewWindow() {
		return newWindow;
	}

	/**
	 * Sets the new window.
	 *
	 * @param newWindow
	 *            the new new window
	 */
	public void setNewWindow(String newWindow) {
		this.newWindow = newWindow;
	}

}
